package programs.integers;

public class SwapTwoNumbers {

	public static void main(String[] args) {
		int a =10;
		int b =20;
		
		//before swapping
		System.out.println(a);
		System.out.println(b);
		
		int c;
		c=a; // c=10
		a=b; // a=20
		b=c; // b=10
		
		//after swapping
		System.out.println(a);
		System.out.println(b);
		
		
	}

}
